void GPIO_init(void)
{
	/* Select general purpose on I/O pins. */
   	P0SEL &= ~(OUT_1_BIT);
	P1SEL &= ~(OUT_2_BIT|OUT_3_BIT);
		
	/* Select GPIO direction : output */
	P0DIR |= OUT_1_BIT;
	P1DIR |= (OUT_2_BIT|OUT_3_BIT);
}
void TURN_off(OUTPUT_TypeDef OUTx)
{
	if(OUTx == 1)
	{
		OUT_1 = 0;
	}
	else if (OUTx ==2)
	{
		OUT_2 = 0;
	}
	else if (OUTx == 3)
	{
		OUT_3 = 0;
	}
}
void TURN_on(OUTPUT_TypeDef OUTx)
{
	if(OUTx == 1)
	{
		OUT_1 = 1;
	}
	else if (OUTx ==2)
	{
		OUT_2 = 1;
	}
	else if (OUTx == 3)
	{
		OUT_3 = 1;
	}
}
void Toggle(OUTPUT_TypeDef OUTx)
{
	if(OUTx == 1)
	{
		OUT_1 = ~OUT_1;
	}
	else if (OUTx ==2)
	{
		OUT_2 = ~OUT_2;
	}
	else if (OUTx == 3)
	{
		OUT_3 = ~OUT_3;
	}
}
uint8 CheckOutputState(OUTPUT_TypeDef OUTx)
{
	if(OUTx == 1)
	{
           if(OUT_1 == 1)
             return 1;
           else
             return 0;
	}
	else if (OUTx ==2)
	{
	   if(OUT_2 == 1)
             return 1;
           else
             return 0;
	}
	else if (OUTx == 3)
	{
	   if(OUT_3 == 1)
             return 1;
           else
             return 0;
	}
}